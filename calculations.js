export function addTwo(num) {
    return num + 2;
}

export function add(num1, num2) {
    return num1 + num2;
}

export function sub(num1, num2) {
    return num1 - num2;
}

export function mult(num1, num2) {
    return num1 * num2;
}

export function div(num1, num2) {
    return num1 / num2;
}

export function mod(num1, num2) {
    return num1 % num2;
}

export function intDiv(num1, num2) {
    return Math.floor(num1 / num2);
}

export function triHyp(num1, num2) {
    return Math.sqrt((num1 * num1) + (num2 * num2));
}

export function coneVol(num1, num2) {
    return Math.PI * ((num1 * num1) * (num2 / 3));
}
